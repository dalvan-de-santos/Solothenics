# Sistema focado em evolução de trinos de calistenia

# O sistema ira permitir que o usuario crie um perfil e suba de nivel de acordo com os treinos realizados.

# O sistema deve ser capaz de rastrear o progresso do usuário ao longo do tempo, fornecendo feedback e 
# sugestões personalizadas para melhorar o desempenho.

# Requisitos do Sistema

# 1. Cadastro de Usuário
#   - O sistema deve permitir que novos usuários se cadastrem com informações básicas, como nome, email e senha.
#   - O sistema deve validar o email para garantir que não haja duplicatas.
# 2. Login de Usuário
#   - O sistema deve permitir que os usuários façam login usando email e senha.
#   - O sistema deve fornecer feedback em caso de falha no login, como senha incorreta ou email não registrado.
# 3. Perfil do Usuário
#   - O sistema deve permitir que os usuários visualizem e editem seu perfil, incluindo informações como nome, email e foto de perfil.
#   - O sistema deve permitir que os usuários visualizem seu histórico de treinos e progresso.
# 4. Registro de Treinos
#   - O sistema deve permitir que os usuários registrem seus treinos, incluindo data, tipo de exercício, duração e intensidade.
#   - O sistema deve permitir que os usuários adicionem anotações ou comentários sobre cada treino.
# 5. Níveis de Progresso
#   - O sistema deve categorizar os usuários em níveis de progresso com base nos treinos realizados.
#   - O sistema deve fornecer feedback sobre o nível atual do usuário e sugestões para avançar para o próximo nível.
# 6. Feedback e Sugestões
#   - O sistema deve analisar o histórico de treinos do usuário e fornecer feedback personalizado sobre o desempenho.
#   - O sistema deve sugerir novos exercícios ou treinos com base no progresso do usuário.
# 7. Segurança
#   - O sistema deve garantir que as senhas dos usuários sejam armazenadas de forma segura, utilizando hashing.
#   - O sistema deve implementar medidas de segurança para proteger os dados dos usuários, como criptografia de dados sensíveis.
# 8. Interface do Usuário
#   - O sistema deve ter uma interface amigável e intuitiva, permitindo fácil navegação entre as funcionalidades.
#   - O sistema deve ser responsivo, funcionando bem em dispositivos móveis e desktops. 
# 9. Documentação
#   - O sistema deve incluir documentação clara e concisa sobre como usar as funcionalidades, incluindo exemplos de uso.


